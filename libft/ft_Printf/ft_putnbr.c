#include "ft_printf.h"

int	ft_putnbr(long long n)
{
	size_t	i;

	i = 0;
	if (n == -2147483648)
		i += write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			i += write(1, "-", 1);
			n *= -1;
		}
		if (n <= 9)
			i += write(1, &(char){n + '0'}, 1);
		if (n > 9)
		{
			i += ft_putnbr(n / 10);
			i += write(1, &(char){n % 10 + '0'}, 1);
		}
	}
	return (i);
}
